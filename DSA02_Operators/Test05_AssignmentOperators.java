package DSA.Operators;

public class Test05_AssignmentOperators {
    public static void main (String[]args)
    {
/*
Assignment Operators: It is used to assign the value on its right to the operands on its left.
 */
        int a=10;
        System.out.println(a+=3); //it will add 3 to 10.
        System.out.println(a-=5);// it will subtract 5 from 13
        System.out.println(a*=2); // it will multiply 8 by 2
        System.out.println(a/=4);//it will divide 16 by 4
    }
}
